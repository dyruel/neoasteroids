#include "CEngine.h"

bool CEngine::init()
{
    SDL_Init(0);
    
    m_videoDevice.init();
    
    m_audioDevice.init();
    
    return true;
}

bool CEngine::shutdown()
{
    m_audioDevice.shutdown();
    
    m_videoDevice.shutdown();
    
    SDL_Quit();
    
    return true;
}



